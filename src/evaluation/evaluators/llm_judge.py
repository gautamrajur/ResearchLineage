# src/evaluation/evaluators/llm_judge.py
from __future__ import annotations

import json
import logging
import re
from typing import Any

from src.evaluation.model_client import ModelClient
from src.evaluation.types import GroundTruth, JudgeScore, LLMJudgeScores

logger = logging.getLogger(__name__)

# ----------------------------------------------------------------- rubric prompt

_RUBRIC_SYSTEM = """\
You are an expert evaluator of scientific research analysis quality.
You will be given a GROUND TRUTH response (generated by a high-quality reference model)
and a PREDICTED response (generated by the model under evaluation).

Score the PREDICTED response on the following four axes, each from 1 to 5:
- factual_grounding: Are the claims consistent with the source material provided?
- logical_coherence: Is the reasoning internally consistent and well-structured?
- domain_consistency: Does the analysis stay within the correct research domain?
- specificity: Does the response avoid vague generalities and give concrete details?

Compute an overall score as the mean of the four axes, rounded to one decimal place.

Respond ONLY with valid JSON in this exact structure:
{
  "factual_grounding": <1-5 float>,
  "logical_coherence": <1-5 float>,
  "domain_consistency": <1-5 float>,
  "specificity": <1-5 float>,
  "overall": <1-5 float>,
  "reasoning": "<your detailed reasoning here>"
}
Do not include any text outside the JSON object.
"""

_RUBRIC_TEMPLATE = """\
FIELD BEING EVALUATED: {field_name}

GROUND TRUTH:
{ground_truth_value}

PREDICTED:
{predicted_value}
"""

# Fields routed to the LLM judge with their extraction paths
_JUDGE_FIELDS: dict[str, tuple[list[str], list[str]]] = {
    # field_name: (gt_path, pred_path)
    "selection_reasoning": (["selection_reasoning"], ["selection_reasoning"]),
    "what_was_improved": (
        ["comparison", "what_was_improved"],
        ["comparison", "what_was_improved"],
    ),
    "how_it_was_improved": (
        ["comparison", "how_it_was_improved"],
        ["comparison", "how_it_was_improved"],
    ),
    "why_it_matters": (
        ["comparison", "why_it_matters"],
        ["comparison", "why_it_matters"],
    ),
    "problem_solved_from_predecessor": (
        ["comparison", "problem_solved_from_predecessor"],
        ["comparison", "problem_solved_from_predecessor"],
    ),
}


def evaluate_llm_judge(
    prediction: dict[str, Any] | None,
    ground_truth: GroundTruth,
    judge_client: ModelClient,
    judge_endpoint_id: str,
) -> LLMJudgeScores:
    """
    Run LLM-as-judge evaluation for all reasoning/analysis fields.
    Each field gets its own judge call so scores are independently auditable.
    The judge's full reasoning is stored verbatim for each field.
    """
    gt_dict = ground_truth.model_dump()
    pred_dict = prediction or {}

    judge_scores: dict[str, JudgeScore] = {}

    for field_name, (gt_path, pred_path) in _JUDGE_FIELDS.items():
        gt_value = _extract_nested(gt_dict, gt_path)
        pred_value = _extract_nested(pred_dict, pred_path)

        score = _judge_single_field(
            field_name=field_name,
            gt_value=gt_value,
            pred_value=pred_value,
            judge_client=judge_client,
        )
        judge_scores[field_name] = score

    return LLMJudgeScores(
        selection_reasoning=judge_scores["selection_reasoning"],
        what_was_improved=judge_scores["what_was_improved"],
        how_it_was_improved=judge_scores["how_it_was_improved"],
        why_it_matters=judge_scores["why_it_matters"],
        problem_solved_from_predecessor=judge_scores["problem_solved_from_predecessor"],
        judge_model_id=judge_endpoint_id,
    )


# ----------------------------------------------------------------- internals


def _judge_single_field(
    field_name: str,
    gt_value: str,
    pred_value: str,
    judge_client: ModelClient,
) -> JudgeScore:
    """Call the judge for one field and parse the rubric response."""
    prompt = _build_judge_prompt(
        field_name=field_name,
        gt_value=gt_value or "(empty)",
        pred_value=pred_value or "(empty — field missing from prediction)",
    )

    try:
        raw_response = judge_client.predict(prompt)
        parsed = _parse_judge_response(raw_response)

        logger.debug(
            "Judge scored field",
            extra={
                "field": field_name,
                "overall": parsed.get("overall"),
            },
        )

        return JudgeScore(
            field_name=field_name,
            score=float(parsed.get("overall", 1.0)),
            reasoning=parsed.get("reasoning", raw_response),
            rubric_axes={
                "factual_grounding": float(parsed.get("factual_grounding", 1.0)),
                "logical_coherence": float(parsed.get("logical_coherence", 1.0)),
                "domain_consistency": float(parsed.get("domain_consistency", 1.0)),
                "specificity": float(parsed.get("specificity", 1.0)),
            },
        )

    except Exception as exc:
        logger.error(
            "Judge call failed for field",
            extra={"field": field_name, "error": str(exc)},
        )
        return JudgeScore(
            field_name=field_name,
            score=1.0,
            reasoning=f"Judge call failed: {exc}",
            rubric_axes={},
        )


def _build_judge_prompt(
    field_name: str,
    gt_value: str,
    pred_value: str,
) -> str:
    body = _RUBRIC_TEMPLATE.format(
        field_name=field_name,
        ground_truth_value=gt_value,
        predicted_value=pred_value,
    )
    return f"{_RUBRIC_SYSTEM}\n\n{body}"


def _parse_judge_response(raw: str) -> dict[str, Any]:
    """
    Parse JSON from the judge's response.
    Strips markdown code fences if present.
    """
    cleaned = raw.strip()

    # strip ```json ... ``` fences
    fence_match = re.search(r"```(?:json)?\s*([\s\S]+?)```", cleaned)
    if fence_match:
        cleaned = fence_match.group(1).strip()

    # find first { ... } block as a fallback
    brace_match = re.search(r"\{[\s\S]+\}", cleaned)
    if brace_match:
        cleaned = brace_match.group(0)

    try:
        return json.loads(cleaned)
    except json.JSONDecodeError as exc:
        logger.warning(
            "Could not parse judge JSON response",
            extra={"error": str(exc), "raw_snippet": raw[:200]},
        )
        return {
            "overall": 1.0,
            "reasoning": f"Parse error: {exc}. Raw response: {raw[:500]}",
        }


def _extract_nested(d: dict[str, Any], path: list[str]) -> str:
    """Traverse a nested dict by key path and return the value as a string."""
    current: Any = d
    for key in path:
        if not isinstance(current, dict):
            return ""
        current = current.get(key, "")
    return str(current) if current is not None else ""
